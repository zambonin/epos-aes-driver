#include <aes.h>
#include <utility/ostream.h>
#include <utility/random.h>

using namespace EPOS;

OStream cout;

#define UMOD(x, y)    (((x) % (y) == 0) ? (y) : (x) % (y))

static const unsigned int KEY_SIZE = 16;
static const unsigned int ITERATIONS = 1024;
static const unsigned short MSG_SIZE = 128;
static const unsigned int MSG_SIZE_MAX =
    MSG_SIZE + KEY_SIZE - UMOD(MSG_SIZE, KEY_SIZE);

typedef struct {
  unsigned char key[KEY_SIZE], in[MSG_SIZE_MAX], out[MSG_SIZE_MAX],
      iv[KEY_SIZE];
} aes_random_test;

typedef struct {
  unsigned char key[KEY_SIZE], in[KEY_SIZE], out[KEY_SIZE], iv[KEY_SIZE];
  AES<KEY_SIZE>::Mode mode;
} aes_known_test;

unsigned int test_known_vectors(), test_random_vectors();

int main() {
  unsigned int seed = Random::random();
  Random::seed(seed);

  cout << "EPOS AES Test" << endl;
  cout << "Random seed = " << seed << endl;
  cout << "Iterations = " << ITERATIONS << endl;
  cout << "Maximum message size = " << MSG_SIZE_MAX << endl << endl;

  return test_random_vectors() + test_known_vectors();
}

unsigned int test_random_vectors() {
  unsigned int i, j, index, fails = 0;
  AES<KEY_SIZE> ecb, cbc(AES<KEY_SIZE>::CBC);
  aes_random_test ex;
  unsigned char orig_iv[KEY_SIZE], orig[MSG_SIZE_MAX];
  bool ok;

  cout << "Testing random vectors... " << endl;
  for (i = 0; i < ITERATIONS; ++i) {
    for (j = 0; j < KEY_SIZE; ++j) {
      ex.key[j] = Random::random();
      ex.iv[j] = Random::random();
    }
    for (j = 0; j < MSG_SIZE_MAX; ++j) {
      ex.in[j] = Random::random();
    }

    // pkcs#7 padding
    for (j = MSG_SIZE; j < MSG_SIZE_MAX; ++j) {
      ex.in[j] = MSG_SIZE_MAX - MSG_SIZE;
    }

    // test AES-ECB-128 mode
    ecb.encrypt(ex.in, ex.key, ex.out, 0, MSG_SIZE_MAX);
    ecb.decrypt(ex.out, ex.key, orig, 0, MSG_SIZE_MAX);

    ok = true;
    for (j = 0; j < MSG_SIZE_MAX; ++j) {
      ok &= (ex.in[j] == orig[j]);
    }

    fails += static_cast<unsigned int>(!ok);

    // it seems like the iv is modified when padding is considered
    for (j = 0; j < KEY_SIZE; ++j) {
      orig_iv[j] = ex.iv[j];
    }

    // test AES-CBC-128 mode
    cbc.encrypt(ex.in, ex.key, ex.out, ex.iv, MSG_SIZE_MAX);
    cbc.decrypt(ex.out, ex.key, orig, orig_iv, MSG_SIZE_MAX);

    ok = true;
    for (j = 0; j < MSG_SIZE_MAX; ++j) {
      ok &= (ex.in[j] == orig[j]);
    }

    fails += static_cast<unsigned int>(!ok);
  }

  cout << "Random vector test has " << fails << " fails." << endl << endl;
  return fails;
}

